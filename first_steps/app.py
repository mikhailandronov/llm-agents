# app.py
import streamlit as st
import pandas as pd # Пример дополнительной библиотеки

# Заголовок приложения
st.title("Мое простое Streamlit приложение")

# Ввод текста от пользователя
name = st.text_input("Введите ваше имя:", "Мир")

# Вывод приветствия
st.write(f"Привет, {name}!")

# Пример использования другого виджета: слайдер
age = st.slider("Выберите ваш возраст:", 0, 100, 25)
st.write(f"Вам {age} лет.")

# Пример отображения данных (если установили pandas)
st.subheader("Пример таблицы данных:")
data = {
    'Имя': ['Алиса', 'Боб', 'Карл'],
    'Возраст': [30, 22, 45],
    'Город': ['Москва', 'СПб', 'Казань']
}
df = pd.DataFrame(data)
st.dataframe(df)

# Кнопка
if st.button("Нажми меня"):
    st.balloons() # Маленький сюрприз
    st.success("Кнопка нажата!")